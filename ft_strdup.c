#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*tmp;
	int		i;

	i = 0;
	while (s1[i])
		i++;
	tmp = (char *)malloc(sizeof(*s1) * (i + 1));
	if (tmp == 0)
		return (0);
	i = 0;
	while (s1[i])
	{
		tmp[i] = s1[i];
		i++;
	}
	tmp[i] = '\0';
	return (tmp);
}
